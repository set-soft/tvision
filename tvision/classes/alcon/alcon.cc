void AlCon_GotoXY(int x, int y)
{
 ASSERT(x > 0 && x <= AlCon_ScreenWidth);
 ASSERT(y > 0 && y <= AlCon_ScreenHeight);
 AlCon_ScareCursor();
 cursorX = x; cursorY = y;
 cursorPX = (x - 1) * AlCon_FontWidth;
 cursorPY = (y - 1) * AlCon_FontHeight;
 AlCon_UnScareCursor();
}

