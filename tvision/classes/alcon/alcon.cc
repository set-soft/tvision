void AlCon_UnDrawCursor(int *aFgCol)
{
 ASSERT(Alcon_CurrentFont);
 ASSERT(aFgCol);
 unsigned offset = (cursorX - 1) + (cursorY - 1) * AlCon_ScreenWidth;
 int bg=attrs[offset]>>4;
 int fg=attrs[offset] & 0xF;
 char b[2];
 b[0]=chars[offset]; b[1]=0;
 AlCon_TextOut(b,cursorPX,cursorPY,fg,bg);
 *aFgCol=colors[fg];
}

void AlCon_IntCursor()
{
 if (cursorEnabled)
   {
    int aFg;
    scare_mouse();
    cursorInScreen=!cursorInScreen;
    AlCon_UnDrawCursor(&aFg);
    if (cursorInScreen)
      {
       if (!(AlCon_CursorBits & ALCON_CURSOR_HIDE_NORMAL))
         textout_ex(screen, &Alcon_CursorFont, "\x1", cursorPX,cursorPY, aFg, -1);

       if (AlCon_CursorBits & ALCON_CURSOR_ON_SQUARE)
         rect(screen, cursorPX, cursorPY, cursorPX + AlCon_FontWidth - 1,
              cursorPY + AlCon_FontHeight - 1, aFg);

       if (AlCon_CursorBits & ALCON_CURSOR_ON_CROSS)
         {
          line(screen, cursorPX, cursorPY, cursorPX + AlCon_FontWidth - 1,
               cursorPY + AlCon_FontHeight - 1, aFg);
          line(screen, cursorPX, cursorPY + AlCon_FontHeight - 1,
               cursorPX + AlCon_FontWidth - 1, cursorPY, aFg);
         }
      }
    unscare_mouse();
   }
}

/**[txh]********************************************************************

  Description: Sets the cursor shape. Pass the start and end lines
  (inclusive) of the cursor. The lines must be in the range 0 to less than
  font's height. Since the start and end are inclusive, you cannot set an
  "invisible" cursor shape. Use AlCon_HideCursor() for that.
  
***************************************************************************/

void AlCon_SetCursorShape(int from, int to)
{
   ASSERT(from <= to);
   ASSERT(from >= 0);
   ASSERT(to < AlCon_FontHeight);
   
   unsigned wb=(AlCon_FontWidth+7)/8;
   memset(Alcon_CursorData[1]->dat, 0, wb*AlCon_FontHeight);
   memset(Alcon_CursorData[1]->dat + wb*from, 0xFF, wb*(to - from + 1));
   AlCon_CursorShapeFrom = from;
   AlCon_CursorShapeTo = to;
}

void AlCon_GetCursorShape(int *from, int *to)
{
   ASSERT(from || to);
   if (from)
      *from = AlCon_CursorShapeFrom;
   if (to)
      *to = AlCon_CursorShapeTo;
}

void Alcon_CreateNewCursor()
{
   Alcon_CursorData[0] =0;
   unsigned wb=(AlCon_FontWidth+7)/8;
   if (Alcon_CursorData[1])
      free(Alcon_CursorData[1]);
   Alcon_CursorData[1] =(FONT_GLYPH *)malloc(sizeof(FONT_GLYPH)+wb*AlCon_FontHeight);
   Alcon_CursorData[1]->w = AlCon_FontWidth;
   Alcon_CursorData[1]->h = AlCon_FontHeight;
   AlCon_SetCursorShape(87 * AlCon_FontHeight / 100, AlCon_FontHeight - 1);
}

