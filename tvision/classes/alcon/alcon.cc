/**[txh]********************************************************************

  Description: Initialization of the AlCon layer. Pass the number of text
  characters you want to have with a default 8x16 font.
  
  Return: Zero if everything went fine, non zero otherwise.
  
***************************************************************************/

int AlCon_Init(int w, int h, int fw, int fh, uchar *fdata, AlCon_Color *pal)
{
   if (fw!=-1 && fh!=-1) {
      AlCon_FontWidth=fw;
      AlCon_FontHeight=fh;
   }

   set_uformat(U_ASCII);
   //set_uformat(U_UTF8);
  
   allegro_init();

   /* Maybe force a specific keyboard layout? */
   if (*AlCon_ForcedKeyboard) {
      AlCon_ForcedKeyboard[3] = 0;
      set_config_string("system", "keyboard", AlCon_ForcedKeyboard);
   }
   
   install_keyboard();
   al_mouse_buttons = install_mouse();
   al_mouse_wheel = mouse_z;
   // Clamp allegro return of "no mouse" to TVision interpretation.
   if (al_mouse_buttons < 0)
      al_mouse_buttons = 0;
   // And add "fake wheel buttons" if the mouse has at least two.
   if (al_mouse_buttons > 1)
      al_mouse_buttons = 5;
   install_timer();
  
   /* If we are in a graphic mode and we know the depth use it */
   if (desktop_color_depth()) {
      set_color_depth(desktop_color_depth());
   } else {
      #ifdef BPP
      set_color_depth(BPP);
      #else
      set_color_depth(8);
      #endif
   }
   
   if (set_gfx_mode(GFX_AUTODETECT_WINDOWED, AlCon_FontWidth * w, AlCon_FontHeight * h,
         AlCon_FontWidth * w, AlCon_FontHeight * h)) {
      set_gfx_mode(GFX_TEXT, 0, 0, 0, 0);
      allegro_message("Can't initialize graphics mode\n%s\n",allegro_error);
      return 2;
   }
   // Set the size of the font and screen.
   AlCon_ScreenWidth =w;
   AlCon_ScreenHeight=h;

   /* Load a binary font. Hack to get the font/screen properties set. */
   ASSERT(fdata);
   AlCon_LoadCustomFont(0,fdata,AlCon_FontWidth,AlCon_FontHeight);
   //AlCon_LoadCustomFont("rom-PC437.016");
  
   /* Create default cursor shape */
   Alcon_CreateNewCursor();
  
   /* Create the text mode palette */
   AlCon_SetDisPaletteColors(0,16,pal ? pal : BIOSPalette);
  
   /* Allocate "screen" buffers */
   AlCon_AllocateMemoryBuffer();
  
   install_int(AlCon_IntCursor, 500);
  
   AlCon_SetColors(7, 0);
   AlCon_ClrScr();
   show_mouse(screen);
   return 0;
}

/**[txh]********************************************************************

  Description: Called when you want to change the font size. You
  can call this first with the font size you would like, and see
  if AlCon is able to resize the screen to that size.
  
  Return: Zero on success. A positive number if the resolution was
  not set, but AlCon was able to restore the previous screen size
  using the old parameters. A negative number if the resolution was
  not set, and trying to set it AlCon lost the previous screen,
  leaving the whole graphic mode in a mess you can only abort
  execution.
  
***************************************************************************/

int AlCon_Resize(unsigned sW, unsigned sH, int new_font_width,
                 int new_font_height)
{
   ASSERT(screen && "Did you really initialise me properly?");
   ASSERT(new_font_width > 0);
   ASSERT(new_font_height > 0);
   ASSERT(AlCon_ScreenWidth > 0);
   ASSERT(AlCon_ScreenHeight > 0);

   // Save here the previous parameters...
   const int old_w = SCREEN_W;
   const int old_h = SCREEN_H;
   const int old_card = gfx_driver->id;

   // Hide the mouse during the change. If we don't do it the cursor gets damaged.
   show_mouse(NULL);
   int temp;
   AlCon_UnDrawCursor(&temp);
   
   // Be brave and try the new screen resolution.
   // TODO: Get rid of explicit driver. Maybe use configuration file?
   int ret = set_gfx_mode(GFX_AUTODETECT_WINDOWED,
      sW * new_font_width, sH * new_font_height,
      sW * new_font_width, sH * new_font_height);

   if (ret == 0)
     {
      AlCon_ScreenWidth=sW;
      AlCon_ScreenHeight=sH;
      // Reallocate internal memory for text video.
      AlCon_AllocateMemoryBuffer();
      // Fix up cursor position, it may have ended up outside screen.
      if (cursorX > AlCon_ScreenWidth || cursorY > AlCon_ScreenHeight)
         AlCon_GotoXY(1, 1);
      
      // Clean up the screen, just in case...
      clear_to_color(screen, 0);
      
      show_mouse(screen);
      return 0;
     }

   // Oops, try to fix our mistake.
   ret = set_gfx_mode(old_card, old_w, old_h, old_w, old_h);
   if (ret != 0)
      return -1;  // Bail out completely.
   show_mouse(screen);
   return 1;   // At least we kept our head safe...
}

/**[txh]********************************************************************

  Description: Removes allegro from the system, freeing any internal
  memory and resetting the graphic mode and other subsystems.
  
***************************************************************************/

void AlCon_Exit()
{
 ASSERT(chars);
 ASSERT(attrs);
 
 remove_int(AlCon_IntCursor);
 set_gfx_mode(GFX_TEXT,0,0,0,0);
 allegro_exit();
 free(chars);
 free(attrs);
 chars = attrs = 0;
}

