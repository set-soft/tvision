void AlCon_SetFont(int which, uchar *fnt, unsigned w, unsigned h)
{
 unsigned sizeChanged=w!=(unsigned)AlCon_FontWidth ||
                      h!=(unsigned)AlCon_FontHeight;
 AlCon_FontWidth =w;
 AlCon_FontHeight=h;
 unsigned wb=(w+7)/8;
 unsigned sizeGlyph=wb*h;

 FONT_GLYPH **p=which ? Alcon_ASCIIDataS : Alcon_ASCIIDataP;
 // Copy font from buffer to internal structure.
 for (int i=0; i<0x100; i++)
    {// Free previously allocated memory.
     if (p[i])
        free(p[i]);
        
     p[i]=(FONT_GLYPH *)malloc(sizeof(FONT_GLYPH)+sizeGlyph);
     p[i]->w=AlCon_FontWidth;
     p[i]->h=AlCon_FontHeight;
 
     memcpy(p[i]->dat,fnt,sizeGlyph);
     fnt+=sizeGlyph;
    }
 Alcon_PrimaryFont.height  =
 Alcon_SecondaryFont.height=
 Alcon_CursorFont.height   =AlCon_FontHeight;

 if (sizeChanged)
    Alcon_CreateNewCursor();
}

/**[txh]********************************************************************

  Description: Loads the specified binary format font into an internal
  structure like Allegro's font and then sets the static global font pointer
  to that. If the font could not be loaded, the previously loaded custom
  font will be used. If this was the first custom font call, Allegro's
  default font will be used if everything fails. If the filename is a NULL
  pointer, the default font will be used.
  
***************************************************************************/

static
void AlCon_LoadCustomFont(const char *filename, uchar *fdata, unsigned fw,
                          unsigned fh)
{
   ASSERT(screen && "You have to call set_gfx_mode before.");
   //static bool one_custom_font_loaded = false;
   unsigned wb=(fw+7)/8;

   FILE *file = NULL;
   if (filename)
      file = fopen(filename, "rb");

   unsigned maxRead=wb*fh*0x100;
   uchar *font_buffer, buffer[maxRead];
   int read_bytes=0;
   if (!file) {
      if (fdata) {
         font_buffer=fdata;
         read_bytes=maxRead;
      } else {
         font_buffer=AlCon_ShapeFont8x16;
         wb=1; fw=8; fh=16;
         read_bytes=4096;
      }
   } else {
      font_buffer=buffer;
      int read_bytes=fread(font_buffer, 1, maxRead, file);
      fclose(file);
      if (read_bytes < 256) {// Absurd size limit
         allegro_message("Absurd custom font size (%d bytes)", read_bytes);
         exit(3);
      }
      //one_custom_font_loaded = true;
   }

   // Initialize global variables.
   Alcon_CurrentFont=&Alcon_PrimaryFont;
   Alcon_PrimaryFont.vtable=font->vtable;
   Alcon_PrimaryFont.data=&Alcon_ASCIIMonoFontP;
   Alcon_SecondaryFont.vtable=font->vtable;
   Alcon_SecondaryFont.data=&Alcon_ASCIIMonoFontS;
   Alcon_CursorFont.vtable=font->vtable;
   Alcon_CursorFont.data=&Alcon_CursorMonoFont;

   AlCon_SetFont(0,font_buffer,fw,read_bytes/(0x100*wb));
}

/**[txh]********************************************************************

  Description: Fills in the width and height of a text cell.
  
***************************************************************************/

void AlCon_GetFontGeometry(unsigned int *w, unsigned int *h)
{
   ASSERT(w || h);
   if (w)
      *w = AlCon_FontWidth;
   if (h)
      *h = AlCon_FontHeight;
}

/**[txh]********************************************************************

  Description: Allocates for the chars and attrs global buffers
  enough memory as specified by the variables AlCon_ScreenWidth
  and AlCon_ScreenHeight. If these global variables already had
  something allocated to them (ie: you are resizing the screen),
  that memory is freed before the new allocation takes place.
  
***************************************************************************/

static void AlCon_AllocateMemoryBuffer(void)
{
   if (chars) {
      ASSERT(attrs);
      free(chars);
      free(attrs);
   }
   chars = (unsigned char *)malloc(AlCon_ScreenWidth * AlCon_ScreenHeight);
   attrs = (unsigned char *)malloc(AlCon_ScreenWidth * AlCon_ScreenHeight);
   ASSERT(chars && attrs);
}

